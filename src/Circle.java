import java.awt.Color;

public class Circle {

    int x;
    int y;
    int width;
    int height;
    Color color;

    Circle (int x, int y, int width, int height, Color c) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
        this.color = c;
    }
    
}